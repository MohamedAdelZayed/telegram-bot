            t = user_data[user_id].get("type")
            if t == "cehrq":
                await handle_cehrq_answer(update, context)
            elif t == "mheoc":
                await handle_mheoc_answer(update, context)
            elif t == "ncdees":
                await handle_ncdees_answer(update, context)
            elif t == "dhss":
                await handle_dhss_answer(update, context)
            else:
                await handle_answer(update, context)


